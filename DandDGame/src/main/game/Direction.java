package main.game;

/**
 * Direction of travel on the game map.
 */
public enum Direction {
	
	/** move up on the map. */
	North,
	
	/** move down on the map. */
	South, 
	
	/** move left on the map. */
	East, 
	
	/** move right on the map. */
	West, 
	
	/** stay in the same position. */
	CurrentPosition;
}
